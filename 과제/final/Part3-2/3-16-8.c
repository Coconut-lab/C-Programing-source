#include <stdio.h>
#include <string.h>   // strcmp 등 문자열 처리 함수를 사용하기 위한 헤더

int main()
{
    // 시스템에 저장된 올바른 사용자 정보
    char id[] = "admin";    // 미리 설정된 관리자 아이디
    char pw[] = "a1234";    // 미리 설정된 관리자 비밀번호

    // 사용자 입력을 저장할 변수들
    char inputId[100];      // 사용자가 입력할 아이디를 저장할 배열
    char inputPw[100];      // 사용자가 입력할 비밀번호를 저장할 배열
    char temp;              // 비밀번호 입력 시 문자를 임시로 저장할 변수
    int i = 0;             // 비밀번호 문자열의 인덱스로 사용할 변수

    // 아이디 입력 받기
    printf("아이디를 입력하세요: ");
    scanf("%s", inputId);   // 사용자로부터 아이디 입력받음

    // 비밀번호 입력 받기 (입력 문자를 *로 표시)
    printf("패스워드를 입력하세요: ");
    // Enter 키가 입력될 때까지 문자를 하나씩 입력받음
    while ((temp = getch()) != '\r')    // getch()로 한 문자 입력받고 Enter키(\r)와 비교
    {
        inputPw[i] = temp;              // 입력받은 문자를 배열에 저장
        i++;                            // 다음 문자를 저장하기 위해 인덱스 증가
        printf("*");                    // 입력된 문자 대신 *을 출력하여 비밀번호를 가림
    }

    inputPw[i] = '\0';                  // 문자열의 끝을 표시하는 널 문자 추가
    printf("\n");                       // 비밀번호 입력 완료 후 줄바꿈

    // 입력된 아이디와 저장된 아이디 비교
    if (strcmp(id, inputId) != 0)       // strcmp는 문자열이 다르면 0이 아닌 값을 반환
    {
        printf("아이디가 일치하지 않습니다.\n");
        return 0;                       // 프로그램 종료
    }

    // 입력된 비밀번호와 저장된 비밀번호 비교
    if (strcmp(pw, inputPw) != 0)       // strcmp는 문자열이 다르면 0이 아닌 값을 반환
    {
        printf("패스워드가 일치하지 않습니다.\n");
        return 0;                       // 프로그램 종료
    }

    // 아이디와 비밀번호가 모두 일치하는 경우
    printf("로그인 성공!\n");

    return 0;   // 프로그램 정상 종료
}